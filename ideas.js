  getRandomIdeas(count = 4) {
    const ideas = [];
    const usedIndices = new Set();
    while (ideas.length < count && usedIndices.size < this.topics.length) {
      const idx = Math.floor(Math.random() * this.topics.length);
      if (usedIndices.has(idx)) continue;
      usedIndices.add(idx);
      const entry = this.topics[idx];
      const format = entry.formatId
        ? this.formats.find(f => f.id === entry.formatId) || this.formats[Math.floor(Math.random() * this.formats.length)]
        : this.formats[Math.floor(Math.random() * this.formats.length)];
      const tips = SHOOTING_TIPS[format.id] || SHOOTING_TIPS.trend;
      ideas.push({
        id: Date.now() + ideas.length,
        topic: entry.topic,
        format: format,
        location: entry.location,
        concept: entry.concept,
        tips: tips,
        source: 'database',
        created: new Date().toISOString(),
        status: 'pending'
      });
    }
    return ideas;
  },

  getRandomQuote() {
    return QUOTES[Math.floor(Math.random() * QUOTES.length)];
  }
